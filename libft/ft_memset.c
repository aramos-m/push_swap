#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*aux;

	aux = str;
	i = 0;
	while (i < n)
	{
		aux[i] = c;
		i++;
	}
	return (str);
}
